package week1_DailyAssignments;

public class D2_ConvertPositive2Negative 
{

	public static void main(String[] args)
	{
		int num = 22;
		
		if(num<0)
		{
			System.out.println("It's a Negative Number");
		}
		num = -(num);
		System.out.println("It's a Positive Number");
		

	}

}
